package Module_1_java;

public class Precedence_operators_math {
    public static void main(String[] args) {
        System.out.println("Precedence operators math");
        System.out.println("Sum: " + (10 + 5));
        System.out.println("Subtraction: " + (10 - 5));
        System.out.println("Multiplication: " + (10 * 5));
        System.out.println("Division: " + (10 / 5));
        System.out.println("Module: " + (10 % 5));
        System.out.println("Module: " + (10 % 3));
        System.out.println("---------------------");
        int calc = 5 + 2 * 3/2; // 5 + 6/2 = 5 + 3 = 8
        System.out.println("Calc: " + calc);
    }
}
