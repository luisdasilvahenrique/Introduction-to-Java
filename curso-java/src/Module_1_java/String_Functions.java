package Module_1_java;

public class String_Functions {
    public static void main(String[] args) {
        String name = "John Doe Silva";
        System.out.println("Tamanho da variavel: " +name.length());
        System.out.println("Segunda posição: " +name.charAt(2));
        System.out.println("Posição da palavra Doe: " +name.indexOf("Doe"));
    }
}
