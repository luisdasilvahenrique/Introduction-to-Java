package Module_1_java;

public class Do_while {
    public static void main(String[] args) {
        int i = 0;
        do {
            System.out.println(i);
            i++;
        } while (i <= 10);
        System.out.println("--------------------");

        int j = 10;
        do {
            System.out.println(j);
            j--;
        } while (j >= 0);
    }
}
